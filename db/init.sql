CREATE TABLE events (
  id SERIAL PRIMARY KEY,
  title TEXT,
  category TEXT,
  date TEXT,
  location TEXT,
  image TEXT,
  description TEXT
);

CREATE TABLE registrations (
  id SERIAL PRIMARY KEY,
  name TEXT,
  email TEXT,
  event_id INTEGER REFERENCES events(id)
);

